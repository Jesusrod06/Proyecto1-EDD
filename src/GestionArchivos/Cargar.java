package GestionArchivos;

import EDD.Grafo;

public class Cargar {
    private Grafo grafo;
    private String txt;

    public Cargar(Grafo grafo, String txt) {
        this.grafo = grafo;
        this.txt = txt;
    }

    public String getTxt() {
        return txt;
    }

    public void setTxt(String txt) {
        this.txt = txt;
    }

    public Grafo getGrafo() {
        return grafo;
    }

    public void setGrafo(Grafo grafo) {
        this.grafo = grafo;
    }
    
    
    
}
