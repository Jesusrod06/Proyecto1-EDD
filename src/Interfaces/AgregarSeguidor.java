    private void ActualizarComboBox() {
        modeloCombo.removeAllElements();
        modeloCombo2.removeAllElements();
        if (!grafo.isEmpty()) {
            for (int i = 0; i < grafo.getUsuarios().getSize(); i++) {
                Vertice verticeActual = (Vertice) grafo.getUsuarios().getValor(i);
                modeloCombo.addElement(verticeActual.getNombre());
            }

            if (modeloCombo.getSize() > 0) {
                principal.setSelectedIndex(0);
                actualizarComboASeguir();
            }
        }
    }

    private void actualizarComboASeguir() {
        modeloCombo2.removeAllElements();

        if (grafo == null || grafo.isEmpty()) {
            return;
        }

        // Usuario seleccionado en el combo principal
        String usuarioPrincipal = (String) principal.getSelectedItem();

        for (int i = 0; i < grafo.getUsuarios().getSize(); i++) {
            Vertice verticeActual = (Vertice) grafo.getUsuarios().getValor(i);
            String nombre = verticeActual.getNombre();

            // ðŸ”¹ No agregamos el usuario principal en el combo de "aSeguir"
            if (!nombre.equals(usuarioPrincipal)) {
                modeloCombo2.addElement(nombre);
            }
        }

        if (modeloCombo2.getSize() > 0) {
            aSeguir.setSelectedIndex(0);
        }
    }

